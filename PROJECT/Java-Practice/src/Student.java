public class Student {
    public String name;
    public int age;
    public int id;
    public String email;
    public String course;
    public String department;

    public Student(String name, int age, int id, String email, String course, String department) {
        this.name = name;
        this.age = age;
        this.id = id;
        this.email = email;
        this.course = course;
        this.department = department;
    }
}